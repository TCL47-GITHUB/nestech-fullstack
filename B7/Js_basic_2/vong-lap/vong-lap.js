// 1. for Loop
console.log("for Loop:");
for (let i = 0; i < 5; i++) {
    console.log(i);
}
// Kết quả: 0, 1, 2, 3, 4

// 2. while Loop
console.log("\nwhile Loop:");
let j = 0;
while (j < 5) {
    console.log(j);
    j++;
}
// Kết quả: 0, 1, 2, 3, 4

// 3. do...while Loop
console.log("\ndo...while Loop:");
let k = 0;
do {
    console.log(k);
    k++;
} while (k < 5);
// Kết quả: 0, 1, 2, 3, 4

