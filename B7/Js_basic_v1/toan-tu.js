      // Toán tử số học
      let a = 5 + 3; // Cộng
      console.log('Cộng:', a); // 8
      
      let b = 5 - 3; // Trừ
      console.log('Trừ:', b); // 2
      
      let c = 5 * 3; // Nhân
      console.log('Nhân:', c); // 15
      
      let d = 6 / 3; // Chia
      console.log('Chia:', d); // 2
      
      let e = 7 % 3; // Chia lấy dư
      console.log('Chia lấy dư:', e); // 1
      
      let f = 2 ** 3; // Lũy thừa
      console.log('Lũy thừa:', f); // 8
      
      // Toán tử so sánh
      console.log('Bằng (==):', 5 == '5'); // true
      console.log('Không bằng (!=):', 5 != '5'); // false
      console.log('Bằng (===):', 5 === '5'); // false
      console.log('Không bằng (!==):', 5 !== '5'); // true
      console.log('Lớn hơn (>):', 5 > 3); // true
      console.log('Nhỏ hơn (<):', 5 < 3); // false
      console.log('Lớn hơn hoặc bằng (>=):', 5 >= 5); // true
      console.log('Nhỏ hơn hoặc bằng (<=):', 5 <= 3); // false
      
      // Toán tử logic
      console.log('Và (&&):', true && false); // false
      console.log('Hoặc (||):', true || false); // true
      console.log('Không (!):', !true); // false
      
      // Toán tử gán
      let x = 10; // Gán
      console.log('Gán:', x); // 10
      
      x += 3; // Gán cộng
      console.log('Gán cộng (+=):', x); // 13
      
      x -= 4; // Gán trừ
      console.log('Gán trừ (-=):', x); // 9
      
      x *= 2; // Gán nhân
      console.log('Gán nhân (*=):', x); // 18
      
      x /= 3; // Gán chia
      console.log('Gán chia (/=):', x); // 6
      
      x %= 2; // Gán chia lấy dư
      console.log('Gán chia lấy dư (%=):', x); // 0
      
